#include <iostream> // Підключаємо бібліотеку для вводу/виводу

using namespace std; // Дозволяємо використовувати стандартні елементи без префікса std::

int main() { // Головна функція програми
    int N, temp, lastDigit, firstDigit, digitsCount = 0; // Оголошуємо змінні
    cout << "Enter a natural number N: "; // Запитуємо користувача ввести натуральне число
    cin >> N; // Зчитуємо введене число
    temp = N; // Зберігаємо значення N у змінну temp для обчислень

    // Рахуємо кількість цифр у N
    do {
        temp /= 10; // Ділимо temp на 10, щоб відкинути останню цифру
        digitsCount++; // Збільшуємо лічильник цифр
    } while (temp > 0); // Продовжуємо, поки temp більше 0

    // Витягуємо першу та останню цифри
    firstDigit = N / (int)pow(10, digitsCount - 1); // Обчислюємо першу цифру
    

// digitsCount — це кількість цифр у числі N.
// pow(10, digitsCount - 1) — це 10, підняте до степеня, що дорівнює кількості цифр, зменшеній на 1. 
// Ця операція дає нам число, яке є найбільшим, яке може бути в межах тієї ж кількості цифр, але з одним нулем в кінці. 
// Наприклад, якщо N = 12345 (5 цифр), то pow(10, 4) = 10000.
// N / (int)pow(10, digitsCount - 1) — це ділення, яке відкидає всі цифри, крім першої, залишаючи тільки її. У нашому прикладі 12345 / 10000 дає 1.





    lastDigit = N % 10; // Обчислюємо останню цифру


// Операція N % 10 — це взяття залишку від ділення N на 10. Вона повертає останню цифру числа N. У нашому прикладі 12345 % 10 дає 5.


    
    // Міняємо місцями першу та останню цифри
    N = (N - firstDigit * (int)pow(10, digitsCount - 1)) + lastDigit * (int)pow(10, digitsCount - 1); // Витягуємо першу цифру і ставимо на її місце останню



// firstDigit * (int)pow(10, digitsCount - 1) — обчислює значення першої цифри в позиції, де вона знаходиться, для N. 
// Наприклад, якщо firstDigit = 1 і digitsCount = 5, то ми отримуємо 1 * 10000 = 10000.
// N - firstDigit * (int)pow(10, digitsCount - 1) — зменшує N на значення, яке ми обчислили, видаляючи першу цифру. Наприклад, 12345 - 10000 дає 2345.
// lastDigit * (int)pow(10, digitsCount - 1) — додає останню цифру на позицію першої. Наприклад, 5 * 10000 дає 50000.
// Отже, після виконання цього коду N зміниться на 2345 + 50000 = 52345.




    
    N = (N - lastDigit) + firstDigit; // Витягуємо останню цифру і ставимо на її місце першу


// N - lastDigit — видаляє останню цифру. У нашому прикладі це буде 52345 - 5 = 52340.
// + firstDigit — додає першу цифру на місце останньої. Отже, 52340 + 1 дає 52341.

    cout << "The number with swapped first and last digits is: " << N << endl; // Виводимо результат

    return 0; // Завершуємо програму
}
